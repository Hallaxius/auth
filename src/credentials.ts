	/** Unique user ID (generated by storage) */
	id: string;
	/** Username (null if strategy doesn't require it) */
	username: string | null;
	/** Email address (null if strategy doesn't require it) */
	email: string | null;
	/** Password hash (managed by PasswordHasher) */
