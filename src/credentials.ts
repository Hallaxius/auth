/**
 * Parse the Cookie header from a Request into a key/value map.
 * Inlined from standalone/cookies.ts.
 */
function parseCookies(request: Request): Record<string, string> {
	const cookieHeader = request.headers.get("cookie") ?? "";
	const cookies: Record<string, string> = {};
	for (const pair of cookieHeader.split(";")) {
		const [key, ...rest] = pair.split("=");
		if (key) {
			cookies[key.trim()] = rest.join("=").trim();
		}
	}
	return cookies;
}

/**
 * Options for creating a session cookie.
 */
interface SessionCookieOptions {
	maxAge?: number;
	path?: string;
	httpOnly?: boolean;
	secure?: boolean;
	sameSite?: "lax" | "strict" | "none";
}

/**
 * Build a Set-Cookie header value for a session cookie.
 * Inlined from standalone/cookies.ts.
 */
function createSessionCookie(
	name: string,
	value: string,
	options: SessionCookieOptions = {},
): string {
	const parts = [`${name}=${value}`];

	if (options.maxAge) parts.push(`Max-Age=${options.maxAge}`);
	if (options.path) parts.push(`Path=${options.path}`);
	if (options.httpOnly) parts.push("HttpOnly");
	if (options.secure) parts.push("Secure");
	if (options.sameSite) parts.push(`SameSite=${options.sameSite}`);

	return parts.join("; ");
}

/**
 * Build a Set-Cookie header value that clears (expires) a session cookie.
 * Inlined from standalone/cookies.ts.
 */
function clearSessionCookie(
	name: string,
	options: {
		path?: string;
		httpOnly?: boolean;
		secure?: boolean;
		sameSite?: "lax" | "strict" | "none";
	} = {},
): string {
	const parts = [
		`${name}=`,
		"Max-Age=0",
		"Expires=Thu, 01 Jan 1970 00:00:00 GMT",
	];

	if (options.path) parts.push(`Path=${options.path}`);
	if (options.httpOnly) parts.push("HttpOnly");
	if (options.secure) parts.push("Secure");
	if (options.sameSite) parts.push(`SameSite=${options.sameSite}`);

	return parts.join("; ");
}

