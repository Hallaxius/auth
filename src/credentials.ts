/**
 * Configuration for the credentials factory.
 */
export interface CredentialsConfig {
	/** Authentication strategy */
	strategy: CredentialsClientConfig["strategy"];
	/** JWT session configuration */
	session: {
		secret: string;
		expiresIn?: string | number;
		cookieName?: string;
	};
	/** User storage implementation */
	storage: AuthUserStorage;
	/** Password hasher implementation */
	hasher: PasswordHasher;
	/** Optional brute force protection */
	bruteForce?: Partial<BruteForceConfig>;
	/** Cookie path (default: "/") */
	cookiePath?: string;
	/** HttpOnly cookie flag (default: true) */
	httpOnly?: boolean;
	/** Secure cookie flag (default: false) */
	secure?: boolean;
	/** SameSite cookie policy (default: "lax") */
	sameSite?: "lax" | "strict" | "none";
}

/**
 * Result of the credentials factory — handlers + session + route guard.
 */
export interface CredentialsResult {
	/** Register a new user */
	handleRegister: (request: Request) => Promise<Response>;
	/** Login with username/email + password */
	handleLogin: (request: Request) => Promise<Response>;
	/** Logout (clear session cookie) */
	handleLogout: (request: Request) => Promise<Response>;
	/** Get current user from session */
	handleMe: (request: Request) => Promise<Response>;
	/** Extract and verify a credentials session from a Request */
	getSession: (request: Request) => Promise<AuthUser | null>;
	/** Protect a route handler — returns 401 if not authenticated */
	withAuth: <
		T extends (
			request: Request,
			ctx: { user: AuthUser },
		) => Promise<Response> | Response,
	>(
		handler: T,
	) => (request: Request) => Promise<Response>;
}

/**
 * Thin credentials factory — wraps createCredentialsHandlers + getSession + withAuth.
 *
 * @example
 * ```ts
 * import { credentials } from "@hallaxius/auth"
 *
 * const auth = credentials({
 *   strategy: "username-email",
 *   session: { secret: process.env.JWT_SECRET!, expiresIn: "7d" },
 *   storage: myStorage,
 *   hasher: bcryptHasher,
 * })
 *
 * // Use in route handlers
 * export const POST = auth.handleRegister
 * export const GET = auth.withAuth(async (req, { user }) => {
 *   return Response.json({ username: user.username })
 * })
 * ```
 */
export function credentials(config: CredentialsConfig): CredentialsResult {
	const cookieName = config.session.cookieName ?? "credentials-session";
	const _cookiePath = config.cookiePath ?? "/";
	const _sameSite = config.sameSite ?? "lax";
	const _secure = config.secure ?? false;
	const _httpOnly = config.httpOnly ?? true;

	// Create the underlying CredentialsClient
	const client = new CredentialsClient(
		{
			strategy: config.strategy,
			secret: config.session.secret,
			expiresIn: config.session.expiresIn,
			cookieName,
		},
		config.storage,
		config.hasher,
		config.bruteForce,
	);

	// Create HTTP handlers (register, login, logout, me)
	const handlers = createCredentialsHandlers({ client });

	/**
	 * Extract and verify a credentials session from a Request.
	 * Returns the AuthUser or null if not authenticated.
	 */
	async function getSession(request: Request): Promise<AuthUser | null> {
		const cookies = parseCookies(request);
		const token = cookies[cookieName];
		if (!token) return null;

		const payload = await verifyToken<Record<string, unknown>>(
			token,
			config.session.secret,
		);
		if (!payload) return null;

		// Look up the full user from storage
		const userId = payload.sub ?? payload.id ?? payload.userId;
		if (typeof userId !== "string") return null;

		const user = await config.storage.findById(userId);
		return user ?? null;
	}

	/**
	 * Protect a route handler — returns 401 if not authenticated.
	 */
	function withAuth<
		T extends (
			request: Request,
			ctx: { user: AuthUser },
		) => Promise<Response> | Response,
	>(handler: T): (request: Request) => Promise<Response> {
		return async (request: Request): Promise<Response> => {
			const user = await getSession(request);
			if (!user) {
				return new Response(JSON.stringify({ error: "Unauthorized" }), {
					status: 401,
					headers: { "Content-Type": "application/json" },
				});
			}
			return handler(request, { user });
		};
	}

	return {
		handleRegister: handlers.handleRegister,
		handleLogin: handlers.handleLogin,
		handleLogout: handlers.handleLogout,
		handleMe: handlers.handleMe,
		getSession,
		withAuth,
	};
}
