/**
 * Base error for the auth error hierarchy.
 * Inlined from core/errors.ts so this file is fully self-contained.
 */
export class DiscordAuthError extends Error {
	readonly code: string;
	readonly cause?: Error;
	readonly statusCode?: number;

	constructor(
		code: string,
		message: string,
		options?: { cause?: Error; statusCode?: number },
	) {
		super(message);
		this.code = code;
		this.cause = options?.cause;
		this.statusCode = options?.statusCode;
		this.name = this.constructor.name;
		if (typeof Error.captureStackTrace === "function") {
			Error.captureStackTrace(this, this.constructor);
		}
	}
}

/** Base error for all credentials auth errors */
export class CredentialsAuthError extends DiscordAuthError {}

