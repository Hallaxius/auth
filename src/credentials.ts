/**
 * Encode a string secret into a Uint8Array key for jose.
 */
function secretToKey(secret: string): Uint8Array {
	return new TextEncoder().encode(secret);
}

/**
 * Sign a JWT payload with HS256.
 * Inlined from standalone/jwt.ts.
 */
async function signToken(
	payload: Record<string, unknown>,
	secret: string,
	expiresIn: string | number = "7d",
): Promise<string> {
	const exp = typeof expiresIn === "number" ? `${expiresIn}s` : expiresIn;

	return new SignJWT(payload)
		.setProtectedHeader({ alg: "HS256" })
		.setIssuedAt()
		.setExpirationTime(exp)
		.sign(secretToKey(secret));
}

/**
 * Verify a JWT token and return its payload, or null if invalid/expired.
 * Inlined from standalone/jwt.ts.
 */
async function verifyToken<T extends Record<string, unknown>>(
	token: string,
	secret: string,
): Promise<T | null> {
	try {
		const { payload } = await jwtVerify(token, secretToKey(secret));
		return payload as T;
	} catch {
		return null;
	}
}

