/**
 * Build a JSON Response with the given status.
 */
function jsonResponse(data: unknown, status = 200): Response {
	return new Response(JSON.stringify(data), {
		status,
		headers: { "Content-Type": "application/json; charset=utf-8" },
	});
}

/**
 * Build a 302 redirect Response, optionally setting cookies.
 */
function redirectResponse(url: string, cookies?: string[]): Response {
	const headers = new Headers();
	headers.set("Location", url);
	if (cookies) {
		for (const c of cookies) headers.append("Set-Cookie", c);
	}
	return new Response(null, { status: 302, headers });
}

/**
 * Convert an error into an appropriate HTTP Response.
 */
function errorResponse(error: unknown): Response {
	if (error instanceof CredentialsAuthError) {
		return jsonResponse(
			{ error: error.message, code: error.code },
			error.statusCode ?? 500,
		);
	}
	return jsonResponse({ error: "Internal server error" }, 500);
}

/**
 * Strip the passwordHash from an AuthUser for safe client responses.
 */
function getSafeUser(user: AuthUser): Record<string, unknown> {
	const { passwordHash: _, ...safe } = user;
	return safe;
}

/**
 * Create the four credentials HTTP handlers (register, login, logout, me).
 * Inlined from standalone/credentials.ts.
 */
function createCredentialsHandlers(ctx: CredentialsHandlerContext) {
	const { client } = ctx;

	const cookieName = "credentials-session";
	const cookiePath = "/";
	const sameSite: "lax" = "lax";
	const secure = false;
	const httpOnly = true;
	const expiresIn = "7d";

	const sessionConfig = {
		type: "jwt" as const,
		secret: "",
		cookieName,
		cookiePath,
		sameSite,
		secure,
		httpOnly,
		expiresIn,
	};

	async function handleRegister(request: Request): Promise<Response> {
		if (request.method !== "POST") {
			return jsonResponse({ error: "Method not allowed" }, 405);
		}

		let body: Record<string, unknown>;
		try {
			body = await request.json();
		} catch {
			return jsonResponse({ error: "Invalid JSON body" }, 400);
		}

		try {
			const result = await client.register(
				{
					username: body.username as string | undefined,
					email: body.email as string | undefined,
					password: body.password as string,
				},
				request,
			);

			const cookie = createSessionCookie(
				cookieName,
				result.token,
				sessionConfig,
			);

			return redirectResponse("/auth/credentials/me", [cookie]);
		} catch (error) {
			return errorResponse(error);
		}
	}

	async function handleLogin(request: Request): Promise<Response> {
		if (request.method !== "POST") {
			return jsonResponse({ error: "Method not allowed" }, 405);
		}

		let body: Record<string, unknown>;
		try {
			body = await request.json();
		} catch {
			return jsonResponse({ error: "Invalid JSON body" }, 400);
		}

		try {
			const result = await client.login(
				{
					username: body.username as string | undefined,
					email: body.email as string | undefined,
				},
				body.password as string,
				request,
			);

			const cookie = createSessionCookie(
				cookieName,
				result.token,
				sessionConfig,
			);

			return redirectResponse("/", [cookie]);
		} catch (error) {
			return errorResponse(error);
		}
	}

	async function handleLogout(_request: Request): Promise<Response> {
		const clearCookie = clearSessionCookie(cookieName, sessionConfig);
		return redirectResponse("/", [clearCookie]);
	}

	async function handleMe(request: Request): Promise<Response> {
		const cookies = parseCookies(request);
		const sessionToken = cookies[cookieName];

		if (!sessionToken) {
			return jsonResponse({ error: "Unauthorized" }, 401);
		}

		try {
			const user = await client.verifySession(sessionToken);
			if (!user) {
				return jsonResponse({ error: "Invalid session" }, 401);
			}
			return jsonResponse(getSafeUser(user));
		} catch {
			return jsonResponse({ error: "Invalid session" }, 401);
		}
	}

	return {
		handleRegister,
		handleLogin,
		handleLogout,
		handleMe,
	};
}

